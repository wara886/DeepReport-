def test_stage11b_pipeline_report_non_empty(tmp_path: Path):
    server, thread = _start_server(_OkHandler)
    try:
        cfg_path = tmp_path / "model_backends.yaml"
        base_url = f"http://127.0.0.1:{server.server_port}/generate"
        _write_backend_config(cfg_path, base_url, timeout=2.0, retry=0)

        output_dir = tmp_path / "outputs"
        report_dir = tmp_path / "reports"
        result = run_pipeline(
            output_dir=str(output_dir),
            report_dir=str(report_dir),
            writer_mode="backend_generate",
            writer_backend="remote",
            writer_backend_config_path=str(cfg_path),
            writer_debug_path=str(report_dir / "writer_debug.json"),
        )

        report_path = Path(result["report_markdown"])
        debug_path = Path(result["writer_debug"])
        assert report_path.exists()
        assert report_path.read_text(encoding="utf-8").strip()
        assert debug_path.exists()
    finally:
        server.shutdown()
        server.server_close()
        thread.join(timeout=1)
